# Въвеждане на брой дни
n = int(input("Въведи брой дни в месеца: "))

# Въвеждане на температурите за всеки ден
arr = []
for i in range(n):
    temp = float(input(f"Температура за ден {i + 1}: "))
    arr.append(temp)

# Въвеждане на стойността k
k = float(input("Въведи температурата k, която търсим: "))

# Преброяване на дните с температура равна на k
br = 0
for temp in arr:
    if temp == k:
        br += 1

# Извеждане на резултата
print(f"Броят на дните с температура {k} е: {br}")
